// ARROW FUNCTIONS ------------------------------------

// Inserir parênteses quando houver 2 ou mais parâmetos
const soma = (num1, num2) => num1 + num2
console.log(soma(5, 10))

// Dispensa parênteses quando há apenas 1 parâmetro
const sayHello = name => `Hello ${name}`
console.log(sayHello('Mouse'))